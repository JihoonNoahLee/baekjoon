#include <iostream>

int main(void) {
  int a, b, c;
  int d, e, f;
  int x, y;

  std::cin >> a >> b >> c >> d >> e >> f;
  for (int i = -999; i <= 999; ++i) {
    for (int j = -999; j <= 999; ++j) {
      if ((a * i + b * j) == c &&
          (d * i + e * j) == f) {
        x = i;
        y = j;
      }
    }
  }
  std::cout << x << ' ' << y;
  return 0;
}
